// for loop 
console.log("------FOR LOOP-----");


for (let i = 0; i < 10; i++) {
    console.log(i);
}

console.log("------WHILE LOOP------");


// while loop

let j = 0;

while (j < 10) {
    console.log(j);
    j++;
}

console.log("-----DO WHILE LOOP");

// do while loop

let k = 0;

do {
    console.log(k);
    k++;
} while (k > 10);
